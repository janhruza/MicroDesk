namespace LibMicroDesk;

/// <summary>
/// Representing the system theme enumeration.
/// </summary>
public enum SystemTheme
{
    /// <summary>
    /// No custom theme is applied.
    /// </summary>
    None,

    /// <summary>
    /// Ligt fluent theme is applied.
    /// </summary>
    Light,

    /// <summary>
    /// Dark fluent theme is applied.
    /// </summary>
    Dark,

    /// <summary>
    /// System theme is applied, which follows the user's system settings.
    /// </summary>
    Auto
}
